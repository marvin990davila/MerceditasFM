    public void showTableProduct() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Id Product");
        model.addColumn("Producto");
        model.addColumn("Id Measure");
        model.addColumn("Medida");
        model.addColumn("Acr√≥nimo");
        tlEmpleados.setModel(model);

        String[] datos = new String[5];
        try {

            Statement leer = conexcion.createStatement();
            ResultSet rs = leer.executeQuery("SELECT pr.ID_PRODUCT,pr.NAME_PRODUCT,pr.ID_MEASURE,me.NAME_MEASURE,me.ACRONYM\n"
                    + "FROM product pr JOIN measure me ON pr.ID_MEASURE=me.ID_MEASURE");

            while (rs.next()) {

                System.out.println(rs.getString(1));
                System.out.println(rs.getString(2));
                System.out.println(rs.getString(3));
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
                datos[4] = rs.getString(5);
                model.addRow(datos);

            }
            tlEmpleados.setModel(model);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "error" + e.toString());
        }
    }

