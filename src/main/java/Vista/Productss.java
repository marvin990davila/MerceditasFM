    private void cargarMedida(JComboBox pp) {
        DefaultComboBoxModel combo = new DefaultComboBoxModel();
        pp.setModel(combo);
        CTMeasure ctc = new CTMeasure();

        try {
            Statement st = conexcion.createStatement();
            ResultSet rr = st.executeQuery("SELECT * FROM measure");

            while (rr.next()) {
                measure con = new measure();
                System.out.println(rr.getString(1));
                con.setId_measure(rr.getInt(1));
                con.setName_measure(rr.getString(2));
                ctc.AgregarMeasure(con);
                combo.addElement(con.getId_measure() + " - " + con.getName_measure());
                System.out.println("Exito....");
            }

        } catch (Exception e) {
            System.out.println("Error, no se puede mostrar combo" + e);
        }
    }
