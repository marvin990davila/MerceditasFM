    public void showTableProduct() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Id Product");
        model.addColumn("Producto");
        model.addColumn("Id Measure");
        model.addColumn("Medida");
        model.addColumn("Acrónimo");
        tlProduct.setModel(model);

        String[] datos = new String[5];
        try {

            Statement leer = conexcion.createStatement();
            ResultSet rs = leer.executeQuery("SELECT pr.ID_PRODUCT,pr.NAME_PRODUCT,pr.ID_MEASURE,me.NAME_MEASURE,me.ACRONYM\n"
                    + "FROM product pr JOIN measure me ON pr.ID_MEASURE=me.ID_MEASURE");

            while (rs.next()) {

                System.out.println(rs.getString(1));
                System.out.println(rs.getString(2));
                System.out.println(rs.getString(3));
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
                datos[4] = rs.getString(5);
                model.addRow(datos);

            }
            tlProduct.setModel(model);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "error" + e.toString());
        }
    }

    public void actualizarProducto() {
        int fila = tlProduct.getSelectedRow();
        System.out.println(fila);

        int idProduct = Integer.parseInt(this.tlProduct.getValueAt(fila, 0).toString());
        String namePro = tlProduct.getValueAt(fila, 1).toString();
        String idpreo = tlProduct.getValueAt(fila, 2).toString();

        System.out.println("La cantidad es: " + namePro);
        System.out.println("El precio es : " + idpreo);

        if (namePro.isEmpty() || idpreo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "!!!Campos vacios por favor llenar registros.");
        } else {
            try {
                PreparedStatement actu = conexcion.prepareStatement("UPDATE product SET NAME_PRODUCT ='" + namePro + "', ID_MEASURE=" + idpreo + " WHERE ID_PRODUCT = " + idProduct + "");
                actu.executeUpdate();
                showTableProduct();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "error" + e.toString());

            }
        }
    }

